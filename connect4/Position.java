
public class Position
{
    private int x;
    private int y;
    Position(int yy, int xx)
    {
        x = xx;
        y = yy;
    }
    /**
     * OMER
     */
    public String toString()
    {
        return "(" + x +", " + y + ")";
    }
    public int getX()
    {
        return x;
    }
    public int getY()
    {
        return y;
    }
}
